import * as _ from "lodash-es";

type TypedArray =
  | Int8Array
  | Uint8Array
  | Int16Array
  | Uint16Array
  | Int32Array
  | Uint32Array
  | Float32Array
  | Float64Array
  | BigInt64Array
  | BigUint64Array
  | Uint8ClampedArray;

export function isTypedArray(value: unknown): value is TypedArray {
  return _.isTypedArray(value);
}
